        #region Importations
        public List<Producto> ImportExcel(string path, int userId)
        {
            List<Producto> lstProducto = [];

            var WorkBook = new XLWorkbook(path);
            var Rows = WorkBook.Worksheet(1).RangeUsed().RowsUsed();

            foreach (var row in Rows)
            {
                var rowNumber = row.RowNumber();

                if (rowNumber > 1)
                {
                    string Nombre = row.Cell(7).GetString();
                    string CodigoProducto = row.Cell(8).GetString();
                    decimal Precio = Convert.ToDecimal(row.Cell(9).GetString());


                    Producto Producto = new()
                    {
                        ProductoId = 0,
                        Active = true,
                        DateTimeCreation = DateTime.Now,
                        DateTimeLastModification = DateTime.Now,
                        UserCreationId = userId,
                        UserLastModificationId = userId,
                        Nombre = Nombre,
                        CodigoProducto = CodigoProducto,
                        Precio = Precio,

                    };

                    lstProducto.Add(Producto);
                }
            }

            return lstProducto;
        }

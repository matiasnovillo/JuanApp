            DataColumn dtColumnCodigoProductoFordtProductoCopy = new()
            {
                DataType = typeof(string),
                ColumnName = "CodigoProducto"
            };
            dtProductoCopy.Columns.Add(dtColumnCodigoProductoFordtProductoCopy);

            DataColumn dtColumnPrecioFordtProductoCopy = new()
            {
                DataType = typeof(string),
                ColumnName = "Precio"
            };
            dtProductoCopy.Columns.Add(dtColumnPrecioFordtProductoCopy);


            #endregion

            dtProductoOriginal = dtProducto;

            foreach (DataRow DataRow in dtProductoOriginal.Rows)
            {
                dtProductoCopy.Rows.Add(DataRow.ItemArray);
            }

            var Sheet = Book.Worksheets.Add(dtProductoCopy);

            Sheet.ColumnsUsed().AdjustToContents();

            Book.SaveAs(path);
        }

        public void ExportToCSV(string path, List<Producto> lstProducto)
        {
